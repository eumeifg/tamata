namespace Magedelight\SocialLogin\Block\Account;

use Magedelight\SocialLogin\Helper\Data as SocialHelper;

class AuthorizationLink extends \Magento\Customer\Block\Account\AuthorizationLink
{
    public function __construct(
        \Magento\Framework\View\Element\Template\Context $context,
        \Magento\Framework\App\Http\Context $httpContext,
        \Magento\Customer\Model\Url $customerUrl,
        \Magento\Framework\Data\Helper\PostHelper $postDataHelper,
        SocialHelper $socialHelper,
        array $data = []
    ) {
        $this->socialHelper = $socialHelper;
        parent::__construct($context, $httpContext, $customerUrl, $postDataHelper, $data);
    }

    public function getDisplayPosition()
    {
        return $this->socialHelper->getDisplayPosition();
    }
}
