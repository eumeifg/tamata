namespace MDC\Commissions\Model\Source;

class VendorGroup implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * Options getter
     *
     * @return array
     */
    public function toOptionArray()
    {
        return [['value' => 1, 'label' => __('VIP')], ['value' => 0, 'label' => __('General')]];
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
