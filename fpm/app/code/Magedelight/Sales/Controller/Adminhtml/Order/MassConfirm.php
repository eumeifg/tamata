namespace Magedelight\Sales\Controller\Adminhtml\Order;

use Magento\Framework\Model\ResourceModel\Db\Collection\AbstractCollection;
use Magento\Backend\App\Action\Context;
use Magento\Ui\Component\MassAction\Filter;
use Magento\Sales\Model\ResourceModel\Order\CollectionFactory;

class MassConfirm extends \Magento\Sales\Controller\Adminhtml\Order\AbstractMassAction
{
    /**
     * @param Context $context
     * @param Filter $filter
     * @param CollectionFactory $collectionFactory
     */
    public function __construct(Context $context, Filter $filter, CollectionFactory $collectionFactory)
    {
        parent::__construct($context, $filter);
        $this->collectionFactory = $collectionFactory;
    }

    /**
     * Confirm selected orders
     *
     * @param AbstractCollection $collection
     * @return \Magento\Backend\Model\View\Result\Redirect
     */
    protected function massAction(AbstractCollection $collection)
    {
        $connection = $collection->getResource()->getConnection();
        if ($collection->getSize()) {
            $updatedRows = $connection->update(
                $collection->getMainTable(),
                ['is_confirmed' => 1],
                [
                    'entity_id in(?)' => $collection->getAllIds()
                ]
            );
            $eventParams = ['order_ids' => $collection->getAllIds()];
            $this->_eventManager->dispatch('vendor_order_admin_mass_confirm', $eventParams);
            $this->messageManager->addSuccess(__('We confirmed %1 order(s).', $updatedRows));
        }
        $resultRedirect = $this->resultRedirectFactory->create();
        $resultRedirect->setPath($this->getComponentRefererUrl());
        return $resultRedirect;
    }
}
