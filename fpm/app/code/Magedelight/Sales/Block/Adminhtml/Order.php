namespace Magedelight\Sales\Block\Adminhtml;

class Order extends \Magento\Backend\Block\Widget\Grid\Container
{
    /**
     * Block constructor
     *
     * @return void
     */
    protected function _construct()
    {
        $this->_controller = 'adminhtml_order';
        $this->_blockGroup = 'Magedelight_Sales';
        $this->_blockRequest = 'Magedelight_Sales';
        $this->_headerText = __('Manage Vendor Order');
        parent::_construct();
        
        $this->removeButton('add');
    }
    
    /**
     * Check permission for passed action
     *
     * @param string $resourceId
     * @return bool
     */
    protected function _isAllowedAction($resourceId)
    {
        return $this->_authorization->isAllowed($resourceId);
    }
}
